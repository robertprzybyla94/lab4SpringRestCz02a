package com.exampleapp.app;

public class Student {
    final  long id;
    final  String name;
    final  String number;
    final String  group;

    public Student (long id, String name, String number, String group)
    {
        this.id=id;
        this.name=name;
        this.number=number;
        this.group=group;
    }
}
